// Get array of all districts
export const getDistricts = () => {
  return Object.keys(karnatakaDistrictsAndTaluks).sort();
};

// Get taluks for a specific district
export const getTaluksByDistrict = (district) => {
  if (!district) return [];
  return karnatakaDistrictsAndTaluks[district.toUpperCase()] || [];
};
